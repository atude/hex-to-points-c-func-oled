function hexToBin(hex: number) {
	const hexStr = hex.toString();
	const binary = (parseInt(hexStr).toString(2)).padStart(8, '0');
	return binary.toString().split("");
}

const hexArray: any = [];

const binaryCompiledStr = () => {
	let currHexStringArr: string[] = [];
	hexes.forEach((hex) => {
		const hexChars = hexToBin(hex);
		for (let i = 0; i < hexChars.length; i++) {
			currHexStringArr.push(hexChars[i]);
		}
		if (currHexStringArr.length === 128) {
			hexArray.push(currHexStringArr.join('')/*.replace(/0/g, '⬛').replace(/1/g, '⬜')*/);
			currHexStringArr = [];
		}
	});
}

const convertHexArrayToPointsAndFunctionString = (name: string): string => {
    binaryCompiledStr();

    const returnStr = [
        `void draw_${name} (void) {`
    ];
    for (let i = 0; i < 32; i++) {
        const hexLineStr = hexArray[i];
        hexLineStr.split('').forEach((num: string, index: number) => {
            if (num === "1") {
                returnStr.push(`    oled_write_pixel(${i}, ${index}, true);`);
            }
        })
    }
    returnStr.push('}');
    return returnStr.join("\n");
}

